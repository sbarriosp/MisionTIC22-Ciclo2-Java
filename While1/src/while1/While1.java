// TODO muestre los numeros de 1 al 100 con la condicion while
//El While siempre trabaja bajo la condocion TRUE. Mientras sea True siempre va a funcionar. 
package while1;

public class While1 {
    public static void main(String[] args) {
        System.out.println("Numeros del 1 al 100 ");
        int i=1; 
        while (i<=100){
            System.out.println(i);
            i++;
        }
    }
    
}
